    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
       int selectRow = jTable1.getSelectedRow();
    if (selectRow != -1) {
        int pizzaId = (int) jTable1.getValueAt(selectRow, 0); // Assuming pizzaID is in the first column
        newPriceText = jTextField1.getText().trim();
        
        // Validate the new price input
        try {
            int newPrice = Integer.parseInt(newPriceText);
            updatePizzaPrice(pizzaId, newPrice);
        } catch (NumberFormatException e) {
            //JOptionPane.showMessageDialog(this, "Please enter a valid price.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    }
    
    public void updatePizzaPrice(int pizzaID, int newPrice) {
    String update = "UPDATE pizza SET price = ? WHERE pizzaID = ?";
    try (PreparedStatement pstmt = con.prepareStatement(update)) {
        pstmt.setInt(1, newPrice);
        pstmt.setInt(2, pizzaID);
        
        int rowsAffected = pstmt.executeUpdate();

        if (rowsAffected > 0) {
            System.out.println("Price updated successfully.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
