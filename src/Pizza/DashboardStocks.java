    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            con = DriverManager.getConnection(url, username, password);
            state = con.createStatement();
            
            String showDetails = "SELECT * FROM pizza";
            result = state.executeQuery(showDetails);
            
            while(result.next()){
                 int pizzaID = result.getInt("pizzaID");
                 String pizzaFlavor = result.getString("pizzaFlavor");
                 int pizzaSizeInch = result.getInt("pizzaSizeInch");
                 int price = result.getInt("price");
                 int stocks = result.getInt("Stocks");
                 
                 model = (DefaultTableModel) jTable1.getModel();

                model.addRow(new Object[]{
                pizzaID,
                pizzaFlavor,
                pizzaSizeInch,
                price,
                stocks
            });
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

